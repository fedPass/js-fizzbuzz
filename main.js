// multipli di 5 stampare "Buzz"
// multipli di 3 e 5 stampare "FizzBuzz"

// Stampare tutti i numeri da 1 a 100
 for (var i = 1; i <= 100; i++) {
     if ([i] % 3 == 0) {
         console.log('Fizz');
     } else if ([i] % 5 == 0) {
         console.log('Buzz');
     }  else {
        console.log([i]);
     }
 }
